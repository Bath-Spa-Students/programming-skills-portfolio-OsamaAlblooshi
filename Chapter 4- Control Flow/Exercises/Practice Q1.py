#Write a python program with if statement that assigns 20 to the variable y and assigns 40 to the variable z if the variable & is greater than 100.
# Assuming you have a variable named 'x'
x = 120  # You can change the value of x as needed

# Using an if statement to check if x is greater than 100
if x > 100:
    y = 20
    z = 40
    print("x is greater than 100. Assigning values to y and z.")
else:
    print("x is not greater than 100. No assignment to y and z.")

# Print the values of y and z
print("y =", y)
print("z =", z)
