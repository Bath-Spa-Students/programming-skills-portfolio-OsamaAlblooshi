#Write a python program that stores fruit names in a list named fruits. Unpack the list into three variables x, y and z and then print the values of each variable.
# Storing fruit names in a list
fruits = ["Apple", "Banana", "Orange"]

# Unpacking the list into variables x, y, and z
x, y, z = fruits

# Printing the values of variables x, y, and z
print("Value of x:", x)
print("Value of y:", y)
print("Value of z:", z)
